// basic syntax of switch statement
// switch (key) {
//     case value:
        
//         break;

//     default:
//         break;
// }


let month = 2
switch (month){
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("February");
        break;
    case 3:
        console.log("March");
        break;

    default:
        console.log("Default case match!");
        break;
}